def candidates() -> list[dict]:
    out = []
    for tmpl, cat, instr, ctx, params in SPECS:
        for p in params:
            out.append({"category": cat, "instruction": instr.format(**p),
                        "context": ctx.format(**p), "code": tmpl.format(**p)})
    return out


def batch_gate(codes: list[str]) -> list[bool]:
    """Один запуск BSL LS на все файлы. Файл без диагностик Error -> прошёл."""
    with tempfile.TemporaryDirectory() as d:
        dp = Path(d)
        for i, c in enumerate(codes):
            (dp / f"n{i}.bsl").write_text(c, encoding="utf-8")
        subprocess.run([JAVA, "-jar", BSL_LS_JAR, "analyze", "--srcDir", str(dp),
                        "--reporter", "json", "--outputDir", str(dp), "--silent"],
                       capture_output=True, timeout=600, check=False)
        report = dp / "bsl-json.json"
        bad = set()
        if report.exists():
            data = json.loads(report.read_text(encoding="utf-8"))
            for fi in data.get("fileinfos", []):
                # honor BSLGEN_GATE_IGNORE like bslgen.gate() — batch must not be
                # stricter than the real gate, or it rejects valid examples (e.g.
                # side-effect Функция without Возврат → FunctionShouldHaveReturn).
                if any(str(x.get("severity", "")).lower() == "error"
                       and x.get("code") not in _GATE_IGNORE
                       for x in fi.get("diagnostics", [])):
                    m = re.search(r"n(\d+)\.bsl", fi.get("path", ""))
                    if m:
                        bad.add(int(m.group(1)))
        return [i not in bad for i in range(len(codes))]


def _existing_codes() -> set:
    p = DATA / "dataset.jsonl"
    if not p.exists():
        return set()
    out = set()
    for line in p.read_text(encoding="utf-8").splitlines():
        if line.strip():
            for m in json.loads(line).get("messages", []):
                if m.get("role") == "assistant":
                    out.add(m["content"])
    return out


def main() -> None:
    cands = candidates()
    oks = batch_gate([c["code"] for c in cands])
    have = _existing_codes()  # идемпотентность: не дописываем то, что уже есть
    added = skipped = 0
    with (DATA / "dataset.jsonl").open("a", encoding="utf-8") as f:
        for c, ok in zip(cands, oks):
            if not ok:
                continue
            if c["code"] in have:
                skipped += 1
                continue
            rec = {"messages": [
                {"role": "user", "content": c["instruction"] + "\nКонтекст: " + c["context"]},
                {"role": "assistant", "content": c["code"]}], "category": c["category"], "synthetic": True}
            f.write(json.dumps(rec, ensure_ascii=False) + "\n")
            added += 1
    print(f"синтетика: гейт-OK {sum(oks)}/{len(cands)}, дописано {added}, дублей пропущено {skipped}")
    for c, ok in zip(cands, oks):
        if not ok:
            print(f"  FAIL [{c['category']}] {c['instruction'][:50]}")


if __name__ == "__main__":
    main()
